# Minhas variáveis

base = float(input('Qual a medida da base do retângulo: '))
altura = float(input('Qual a medida da altura  do retângulo: '))

# Formula do perímetro de um retângulo

perimetro = 2 * (base + altura)

# Fórmula da área de um retângulo

area = base * altura

print('O perímetro do retângulo é igual a',perimetro,'metros')
print('E sua área é de', area,'mestros quadrados')